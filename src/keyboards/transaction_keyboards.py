## –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –≤—ã–±–æ—Ä–∞ —Ç–∏–ø–∞ –æ–ø–µ—Ä–∞—Ü–∏–∏
def get_transaction_type_keyboard() -> InlineKeyboardMarkup:
    """
    –°–æ–∑–¥–∞–µ—Ç inline-–∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –¥–ª—è –≤—ã–±–æ—Ä–∞ —Ç–∏–ø–∞ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏.
    
    Returns:
        InlineKeyboardMarkup: –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –∫–Ω–æ–ø–∫–∞–º–∏ "–î–æ—Ö–æ–¥" –∏ "–†–∞—Å—Ö–æ–¥"
    """
    builder = InlineKeyboardBuilder()
    builder.row(
        InlineKeyboardButton(text="üí∞ –î–æ—Ö–æ–¥", callback_data="type:income"),
        InlineKeyboardButton(text="üí∏ –†–∞—Å—Ö–æ–¥", callback_data="type:expense")
    )
    builder.row(
        InlineKeyboardButton(text="‚ùå –û—Ç–º–µ–Ω–∞", callback_data="cancel")
    )
    return builder.as_markup()


## –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –≤—ã–±–æ—Ä–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
def get_categories_keyboard(categories: List[tuple], transaction_type: str) -> InlineKeyboardMarkup:
    """
    –°–æ–∑–¥–∞–µ—Ç inline-–∫–ª–∞–≤–∏–∞—Ç—É—Ä—É –¥–ª—è –≤—ã–±–æ—Ä–∞ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏.
    
    Args:
        categories: –°–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π (id, name, emoji) –∫–∞—Ç–µ–≥–æ—Ä–∏–π
        transaction_type: –¢–∏–ø —Ç—Ä–∞–Ω–∑–∞–∫—Ü–∏–∏ ('income' –∏–ª–∏ 'expense')
    
    Returns:
        InlineKeyboardMarkup: –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º–∏
    """
    builder = InlineKeyboardBuilder()
    
    for category_id, name, emoji in categories:
        button_text = f"{emoji} {name}"
        builder.button(
            text=button_text,
            callback_data=f"category:{category_id}"
        )
    
    # 2 –∫–Ω–æ–ø–∫–∏ –≤ —Ä—è–¥ –¥–ª—è –∫–æ–º–ø–∞–∫—Ç–Ω–æ—Å—Ç–∏
    builder.adjust(2)
    
    # –ö–Ω–æ–ø–∫–∞ "–î—Ä—É–≥–æ–µ" –∏ "–û—Ç–º–µ–Ω–∞"
    builder.row(
        InlineKeyboardButton(text="‚úèÔ∏è –î—Ä—É–≥–æ–µ", callback_data="category:custom")
    )
    builder.row(
        InlineKeyboardButton(text="‚ùå –û—Ç–º–µ–Ω–∞", callback_data="cancel")
    )
    
    return builder.as_markup()


## –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ –ø–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏—è
